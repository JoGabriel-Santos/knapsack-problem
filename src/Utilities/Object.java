package Utilities;

public class Object {
    Integer id, weight, value;

    public Object(Integer id, Integer weight, Integer value) {
        this.id = id;
        this.weight = weight;
        this.value = value;
    }

    public Integer getId() {
        return id;
    }

    public Integer getWeight() {
        return weight;
    }

    public Integer getValue() {
        return value;
    }
}
